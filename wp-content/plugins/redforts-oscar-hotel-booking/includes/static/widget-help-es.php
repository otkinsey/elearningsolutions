<h1>Widgets</h1>
<p>
    Puede agregar un widget de reservas en cualquiera de las áreas de widgets de
    las que disponga en Wordpress. Puede ver los widgets y sus áreas de widgets
    disponibles en el menú administrativo de Wordpress <a href="widgets.php"><?php _e('Appearance'); ?> - <?php _e('Widgets'); ?></a>.
</p>

<p>
    Para añadir un widget de reservas, simplemente arrastre el widget
    <strong>Redforts booking widget</strong> al área de widgets donde quiera que
    aparezca. Una vez situado en el área, configúrelo a su medida.
</p>
